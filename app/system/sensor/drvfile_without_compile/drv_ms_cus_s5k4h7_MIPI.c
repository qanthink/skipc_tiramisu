{
    int i;
    switch(mode)
    {
    case 1:
        PatternTbl[0].data = 0x0002; //enable
        break;
    case 0:
    default:
        PatternTbl[0].data = 0x0000; //disable
        break;
    }
    for(i=0;i< ARRAY_SIZE(PatternTbl);i++)
    {
        if(SensorReg_Write(PatternTbl[i].reg,PatternTbl[i].data) != SUCCESS)
        {
            SENSOR_EMSG("[%s:%d]Sensor init fail!!\n", __FUNCTION__, __LINE__);
            return FAIL;
        }
    }
    return SUCCESS;
}

static int pCus_init_12M_mipi4lane_linear(ms_cus_sensor *handle)
{	
    int i,cnt=0;

    if(pCus_CheckSensorProductID(handle) == FAIL) {
        return FAIL;
    }

    for(i = 0; i < ARRAY_SIZE(Sensor_init_table_12M_4lane_linear); i++)
    {
        if(Sensor_init_table_12M_4lane_linear[i].reg == 0xffff)
        {
            SENSOR_MSLEEP(Sensor_init_table_12M_4lane_linear[i].data);
        }
        else
        {
            cnt = 0;
            while(SensorReg_Write(Sensor_init_table_12M_4lane_linear[i].reg, Sensor_init_table_12M_4lane_linear[i].data) != SUCCESS)
            {
                cnt++;
                if(cnt >= 10)
                {
                    SENSOR_EMSG("[%s:%d]Sensor init fail!!\n", __FUNCTION__, __LINE__);
                    return FAIL;
                }
                //SENSOR_UDELAY(1);
            }
        }
    }

    SensorReg_Write(0x0202, 0x0C7A);
    SensorReg_Write(0x0200, 0x0100);

    handle->i2c_cfg.fmt = I2C_FMT_A16D8;
    SensorReg_Write(0x0100, 0x01);
    handle->i2c_cfg.fmt = SENSOR_I2C_FMT;

    return SUCCESS;
}

