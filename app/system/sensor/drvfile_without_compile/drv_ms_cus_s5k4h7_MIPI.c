//AE status notification
static int pCus_AEStatusNotify(ms_cus_sensor *handle, CUS_CAMSENSOR_AE_STATUS_NOTIFY status)
{
    s5k4h7_params *params = (s5k4h7_params *)handle->private_data;
    //ISensorIfAPI2 *sensor_if1 = handle->sensor_if_api2;

    switch(status)
    {
        case CUS_FRAME_INACTIVE:
            if(params->orien_dirty) {
                //SensorReg_Write(0x0104,1); // Global hold on
                DoOrien(handle, handle->orient);
                params->orien_dirty = false;
                //SensorReg_Write(0x0104,0); // Global hold off
            }
             break;
        case CUS_FRAME_ACTIVE:
            if(params->dirty || params->orien_dirty) {
                //SensorReg_Write(0x0104,1); // Global hold on
                if(params->dirty) {
                    SensorRegArrayW((I2C_ARRAY*)params->tExpo_reg, ARRAY_SIZE(expo_reg));
                    SensorRegArrayW((I2C_ARRAY*)params->tGain_reg, ARRAY_SIZE(gain_reg));
                    SensorRegArrayW((I2C_ARRAY*)params->tVts_reg, ARRAY_SIZE(vts_reg));
                    params->dirty = false;
                    SensorReg_Write(0x010f, 1);
                }

                if(params->orien_dirty) {
                    DoOrien(handle, handle->orient);
                    params->orien_dirty = false;
                }
                //SensorReg_Write(0x0104,0); // Global hold off
            }
            break;
        default :
             break;
    }
    return SUCCESS;
}

static int pCus_GetAEUSecs(ms_cus_sensor *handle, u32 *us)
{
    u32 lines = 0;
    s5k4h7_params *params = (s5k4h7_params *)handle->private_data;

    lines |= (u32)(params->tExpo_reg[0].data);

    *us = (lines * Preview_line_period) / 1000;

    //CamOsPrintf("-----> sensor expo lines/us %u,%u us\n", lines, *us);

    return SUCCESS;
}

static int pCus_SetAEUSecs(ms_cus_sensor *handle, u32 us)
{
    u32 expo_lines = 0, vts = 0;
    u32 new_us=0;
    s5k4h7_params *params = (s5k4h7_params *)handle->private_data;

    new_us = us;
    params->expo.expo_lef_us = new_us;
    expo_lines = (1000 * new_us) / Preview_line_period;
    //CamOsPrintf(">>>> expo_lines: %d , params->expo.vts: %d\n",expo_lines,params->expo.vts);

    if (expo_lines > params->expo.vts)
        vts = expo_lines + 8;
    else
        vts = params->expo.vts;

    params->expo.expo_lines = expo_lines;

    params->tExpo_reg[0].data = expo_lines;
    params->tVts_reg[0].data = vts;
    //CamOsPrintf(">>>> pCus_SetAEUSecs  [new_us:%d , prev:%d , vts:%d] --tExpo_reg: 0x%04x,  tVts_reg: 0x%04x \n",new_us,Preview_line_period,params->expo.vts,params->tExpo_reg[0].data,params->tVts_reg[0].data);
    params->dirty = true;
    return SUCCESS;
}

