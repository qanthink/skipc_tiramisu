static int pCus_SetAEGain(ms_cus_sensor *handle, u32 gain)
{
    s5k4h7_params *params = (s5k4h7_params *)handle->private_data;

    u32 real_gain = 0;
    u32 a_gain = 0;
    u32 d_gain = 0;
    if(gain < SENSOR_MIN_GAIN) {
        gain = SENSOR_MIN_GAIN; 
    } else if(gain >= SENSOR_MAX_GAIN) {
        gain = SENSOR_MAX_GAIN;
    }

    real_gain = gain/32;

    if (real_gain < 16 * 32) {
        a_gain = real_gain;
        d_gain = 256;
    } else {
        a_gain = 16 * 32;
        d_gain = 256.0 * real_gain / a_gain;

        if ((uint32_t)d_gain > 16 * 256)
            d_gain = 16 * 256; // d_gain < 256x
    }
    params->expo.final_gain = gain;

    params->tGain_reg[0].data = a_gain;
    params->tGain_reg[1].data = d_gain;
    params->tGain_reg[2].data = d_gain;
    params->tGain_reg[3].data = d_gain;
    params->tGain_reg[4].data = d_gain;

    //SensorReg_Write(0x010f, 1);

    //CamOsPrintf(">>>pCus_SetAEGain>>gain:%d  real_gain:%d, again:0x%x   dgain:0x%04lx\n",gain,real_gain,a_gain,d_gain);

    //printk("[%s] A gain/reg=%u/0x%x 0x%x;D gain/reg=0x%x 0x%x \n", __FUNCTION__, gain,params->tGain_reg[0].data, params->tGain_reg[1].data, params->tGain_reg[2].data, params->tGain_reg[3].data);
    params->dirty = true;
    return SUCCESS;
}

